public class GradeCal {
    public static void main (String[] args){
        int marks = 73;
        if (marks>=75){
            char grade = 'A';
            System.out.println(grade);
        }
        else if (marks>=60) {
            char grade = 'B';
            System.out.println(grade);
        }
        else if (marks>=50) {
            char grade = 'C';
            System.out.println(grade);

        }


    }
}
